package com.MusicApp.Entity.JsonClasses.For_TopAlbums;

public class Album {

    private String name;
    private String url;

    public Album () {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
